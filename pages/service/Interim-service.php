<h3 class="tittle"> INTERIM SERVICE INCLUDES:</h3>
<ul class="service-ul">
	<i>Engine</i>
		<li>Drain engine oil and refill.</li>
		<li>Replace oil filter</li>
		<li>Check for excessive oil leaks.</li>
		<li>Check for timing belt </li>
		<li>Check condition and security of undertray.</li>
	<i>Drive System</i>
		<li>Check for correct clutch operation.</li>
		<li>Check for correct gearbox operation.</li>
		<li>Check and top up clutch fluid where applicable.</li>
		<li>Check drive shaft gaitors for security and report leaks.</li>
	<i>Electrical</i>
		<li>Check operation of exterior lights</li>
		<li>Check battery condition and print results.</li>
		<li>Check dashboard warning lights.</li>
	<i>Steering and Suspension</i>
		<li>Check and top up power steering reservoir where applicable.</li>
		<li>Check steering and suspension components for wear and corrosion.</li>
		<li>Check steering rack gaiters condition.</li>
		<li>Check wheel bearings for excessive play or roughness.</li>
		<li>Carry out shock absorber bounce test.</li>
	<i>Exhaust</i>
		<li>Inspect exhaust system (including catalyst if fitted) for leaks, security and noise.</li>
		<li>Visual check for exhaust smoke.</li>
	<i> Brakes</i>	
		<li>Check brake pads for wear or damage.</li>
		<li>Check brake callipers for leaks and security.</li>
		<li>Check service brake shoes for wear or damage - clean and adjust if required </li>
		<li>Inspect operation of wheel cylinders and report any leaks.</li>
		<li>Visually check brake hydraulic system, pipes and hoses for any leaks, chafing and corrosion.</li>
		<li>Check security of handbrake linkages and travel. Lubricate and adjust if required.</li>
		<li>Check brake discs and drums for wear, cracks, corrosion and scoring or pitting </li>
		<li>Check brake fluid level and top up if required.</li>
	<i>Tyres and Wheels</i>
		<li>Remove road wheels.</li>
		<li>Check tyre size and whether tyre is fitted to side wall instructions.</li>
		<li>Check tyre condition and tread depth (including spare).</li>
		<li>Check and adjust tyre pressures (including spare).</li>
		<li>Set wheel nut torque to manufacturer's settings.</li>
		<li>Check wheels for correct balance (including spare).</li>
	<i>Vision	</i>
		<li>Check windscreen wiper condition and operation.</li>
		<li>Check rear wiper condition and operation (if fitted).</li>
		<li>Check windscreen washer operations, align jets and top up if required.</li>
	<i>Internal</i>
		<li>Advise if cabin filter is required according to age/mileage recommendations and last change.</li>
	<i>General</i>
		<li>Reset vehicle service light where applicable </li>
		<li>Conduct road test.</li>
		<li>Stamp service book</li>
</ul>
